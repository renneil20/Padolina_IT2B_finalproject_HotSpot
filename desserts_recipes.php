                <h3>Yakgwa (약과; 藥菓)</h3>
                <p><strong>Ingredients:</strong></p>
                <p><strong>For Dough:</strong></p><br>
                    <em>1.</em> 3 cup all-purpose flour (medium-protein flour)<br>
                    <em>2.</em> 1/3 cup sesame oil<br>
                    <em>3.</em> 1/3 cup soju<br>
                    <em>4.</em> 1/3 cup honey<br>
                    <em>5.</em> 1/4 cup chopped pine nuts<br>
                    <em>6.</em> 1/4 tsp salt (optional – original recipe omits salt)</p>

                <p><strong>For Syrup:</strong><br>
                    <em>1.</em> 1 cup rice syrup (jochung 조청 – if not available, you can substitute honey)<br>
                    <em>2.</em> 1 cup honey<br>
                    <em>3.</em> 2 cup water<br>
                    <em>4.</em> 1 Tbsp ginger (grated, 1 -2 tsp ginger powder is OK too)</p>

                <p><strong>Instructions:</strong><br>
                    <em>1.</em> Prepare oil for frying. Note for Yakgwa, we want to start frying at a low temperature.<br>
                    <em>2.</em> Make syrup by mixing rice syrup, honey, and water in a saucepan or frying pan. Heat on medium heat, stirring occasionally. Once it boils, turn the heat off and add the chopped ginger, mix and set it aside.<br>
                    <em>3.</em> In a large mixing bowl, add flour (add salt – optional) and then sesame oil. Mix the flour with the oil by rubbing the mixture thoroughly with your hands, until you feel the oil is all blended in with the flour.<br>
                    <em>4.</em> Mix the Soju and honey in a separate cup/bowl until the honey is all dissolved.<br>
                    <em>5.</em> Pour the soju/honey mixture into the flour mixture and gently press the dough to form a ball.<br>
                    <em>6.</em> Divide the dough in half. Roll out one-half of the dough into a 1/4 to 1/3 inch thick rectangular shape. Cut the dough into 1 inch wide strips and then cut the strips at an angle to make diamond shapes.<br>
                    <em>7.</em> Yakgwa is fried in 2 different temperatures and this is what makes it tricky. For the first part, oil should be around 265° F (130°C). Yakwa will slowly rise to the top.<br>
                    <em>8.</em> For the second part, when all the pieces have risen to the top, raise the heat to medium-high to reach normal frying temperature. Continue frying until Yakgwa turns rich brown in color.<br>
                    <em>9.</em> When Yakgwa pieces turn rich dark brown, remove them from the oil and let the oil drain in a colander lined with a paper towel.<br>
                    <em>10.</em> Soak fried Yakgwa in prepared ginger honey syrup.<br>
                    <em>11.</em> Leave for a few min until it has soaked up all the syrup, take it out of the syrup and place it onto a plate.<br>
                    <em>12.</em> Sprinkle top with chopped pine nuts while it is still wet so the nuts will stick to it.</p>
