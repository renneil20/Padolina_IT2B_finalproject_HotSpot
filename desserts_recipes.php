                <h3>SONGPYEON (RICE CAKES)</h3>
                <p><strong>Ingredients: <br>
                    Rice Powder (ssalgaru)</strong><br>
                        <em>1.</em> 2 cups wet rice powder per batch (about 16 songpyeon)<br>
                        <em>2.</em> 4 to 5 ounces pine needles<br>
                        <em>3.</em> 1 teaspoon sesame oil</p>

                <p><strong>Pink dough:</strong><br>
                        <em>1.</em> 2 cups frozen rice powder<br>
                        <em>2.</em> 4 tablespoons raspberry juice</p>

                <p><strong>Filling: </strong><br>
                        <em>1.</em> ⅓ cup roasted sesame seeds<br>
                        <em>2.</em> 1.5 tablespoons sugar<br>
                        <em>3.</em> 1 tablespoon honey<br>
                        <em>4.</em> pinch salt</p>

                <p><strong>Instructions: Dough</strong><br>
                    <em>1.</em> For each type of dough, sift the rice powder into a bowl. Prepare the flavoring and liquid ingredients (water/juice/mugwort powder/kabocha) for each selected color of dough. Add the specified amount of the solid and/or liquid (making sure it’s boiling hot) to the rice powder, quickly stirring them in with a spoon. (Tip: Hold a teaspoon or two of the liquid and make sure more liquid is needed before adding it.)<br>
                    <em>2.</em> Knead, pressing and stretching with the heel of the hand. Fold and rotate the dough. Repeat the kneading process for 4 to 5 minutes. (If the dough sticks to your hands or the bowl after some kneading, it’s too wet. Add more rice powder in 1 tablespoon increments. If the dough breaks easily or is too stiff, it’s too dry. Add more boiling hot water in 1 teaspoon increments.)<br>
                    <em>3.</em> Cover the dough with plastic wrap or a damp towel. Let it rest for 20 to 30 minutes.</p>

                <p><strong>Filling:</strong><br>
                    <em>1.</em> Roughly grind the sesame seeds in a grinder or blender. Add the sugar, honey and pinch of salt. Mix everything well together until the sugar and honey are evenly distributed.</p>

                <p><strong>Assemble:</strong><br>
                    <em>1.</em> To assemble, tear off a small piece of the dough enough to make roughly 1-inch ball. Roll tightly between your palms to shape a ball.<br>
                    <em>2.</em> Make a well in the ball by pressing into it with your thumb, and press outwards, with both of your thumbs, on the inside walls of the well to slightly expand the opening.><br>
                    <em>3.</em> Place 1/2 teaspoon of the filling in the well. Seal tightly by squeezing the edges together. Squeeze the whole rice cake lightly in your palm to firm it up, and then gently roll between your palms to smooth out the surface. Shape it into a nice crescent shape. Pinch along to create a wing. Repeat until all the dough/filling is used.</p>

                <p><strong>Steam and finish:</strong><br>
                    <em>1.</em> Boil water in a steamer. Place a thin layer of pine needles or a silicon liner or damp kitchen cloth in the steamer insert. Boil the water over high heat. When it starts to steam, place the rice cakes in the steamer insert without the pieces touching one another. Cover with a thin layer of pine needles, if available. Cover and steam for about 20 minutes.<br>
                    <em>2.</em> Prepare a big bowl of cold water. Using a long-handed spoon or tong, remove the rice cakes from the steamer. Drop them in the cold water for a quick rinse, removing all the pine needles if used. Transfer the rice cakes to a colander to drain. Lightly coat your hands with the sesame oil and rub the oil on the rice cakes in batches. Serve warm or at room temperature</p>
