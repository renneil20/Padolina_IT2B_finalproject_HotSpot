                <h3>Yaksik (Sweet Rice with Dried Fruits and Nuts)</h3>
                <p><strong>Ingredients:</strong><br>
                    <em>1.</em> 2 cups sweet rice (aka glutinous rice), chapssal (찹쌀) Standard measuring cup before soaking<br>
                    <em>2.</em> 4 ounces peeled fresh, frozen, or canned chestnuts - about 12 to 15 whole chestnuts depending on the sizes ½ to ¾ cup cut pieces<br>
                    <em>3.</em> 2.5 ounces jujubes (daechu, 대추) - about 10 to 15 depending on sizes ½ cup, packed, cut pieces<br>
                    <em>4.</em> 1 tablespoon pine nuts - optional<br>
                    <em>5.</em> 1 tablespoon pumpkin seeds - optional</p>

                <p><strong>Seasonings:</strong><br>
                    <em>1.</em> 2 tablespoons soy sauce<br>
                    <em>2.</em> 1/2 cup dark brown sugar use ⅓ cup for less sweet yaksik<br>
                    <em>3.</em> 2 tablespoons honey or use about 2 to 2.5 tablespoons more brown sugar<br>
                    <em>4.</em> 2 tablespoons sesame oil<br>
                    <em>5.</em> 1/4 teaspoon cinnamon powder - optional</p>

                <p><strong>Instructions:</strong><br>
                    <em>1.</em> Rinse the sweet rice 3 or 4 times until the water is fairly clear. Soak the rice for an hour or up to 2 hours. Drain well while preparing the other ingredients. Tap the strainer to shake off any excess water.<br>
                    <em>2.</em> Meanwhile, rinse the jujubes. Cut around the seed of each jujube to separate the flesh, and then cut the flesh into small pieces. Reserve the seeds to make jujube water.<br>
                    <em>3.</em> To make jujube flower decorations, roll the flesh tightly, and cut into thin slices.<br>
                    <em>4.</em>In a small saucepan, boil 3 cups of water and the jujube seeds over medium high heat until the liquid is reduced to 1.5 cup (15 to 20 minutes). Measure this for the accurate ratio of rice and water.<br>
                    <em>5.</em> To 1.5 cup of jujube flavored water, add the soy sauce, sugar, and honey. Bring it to a boil and continue to boil for a minute, which will yield about 1 and 3/4 cups of seasoned water. Add the sesame oil.</p>
