                <h3>Dalgona (달고나)</h3>
                <p><strong>Ingredients:</strong><br>
                    <em>1.</em> Sugar<br>
                    <em>2.</em> Baking Soda</p>

                <p><strong>Instructions:</strong><br>
                    <em>1.</em> Add two tablespoons of sugar to a metal ladle, then place directly over medium heat.<br>
                    <em>2.</em> Stir constantly with a wooden chopstick until the sugar is melted and a light amber color.<br>
                    <em>3.</em> Remove the ladle from the heat, add a pinch of baking soda, and mix vigorously until incorporated.<br>
                    <em>4.</em> Pour the mixture onto parchment paper in two equal circles.<br>
                    <em>5.</em> Use a lightly oiled measuring cup to press the candy into a thin, even layer a bit less than ¼ inch.<br>
                    <em>6.</em>Let the measuring cup rest there for about 8 seconds, then remove and press a shape in with your cookie cutter. Repeat for the remaining portion of candy.</p>
