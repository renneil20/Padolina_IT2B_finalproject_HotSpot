                <h3>Ramyeon 라면</h3>
                <p>Korean ramen</p>
                <p><strong>Ingredients:</strong><br>

                    <em>1.</em> 1 package of Korean ramyeon<br>
                    <em>2.</em> 2½ cups water<br>
                    <em>3.</em> 1 egg<br>
                    <em>4.</em> 2 green onions, chopped</p>

                <p><strong>Directions:</strong><br>

                    <em>1.</em>  Bring the water to a boil over high heat.<br>

                    <em>2.</em>  Add the noodles and the included soup powder to the boiling water. Cover and boil for 1 minute.<br>

                    <em>3.</em>  Open the lid and turn the ramyeon over with a spoon.<br>

                    <em>4.</em>  Crack the egg directly into the boiling ramyeon, and cover and cook for 1 to 2 minutes. Don’t stir, so the egg will be poached.<br>

                    <em>5.</em>  Remove from the heat. Open the lid and add the green onions and the included package of dried vegetables. Serve with kimchi and rice.</p>
